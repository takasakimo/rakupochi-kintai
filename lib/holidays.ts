/**
 * 指定された日付が祝日かどうかを判定
 * @param date 判定する日付
 * @returns 祝日の場合true
 */
export function isHoliday(date: Date): boolean {
  const year = date.getFullYear().toString()
  const dateStr = date.toISOString().split('T')[0]
  
  if (holidays[year]) {
    return holidays[year].includes(dateStr)
  }
  
  return false
}

/**
 * 指定された日付が日曜日かどうかを判定
 * @param date 判定する日付
 * @returns 日曜日の場合true
 */
export function isSunday(date: Date): boolean {
  return date.getDay() === 0
}

/**
 * 指定された日付が祝日または日曜日かどうかを判定
 * @param date 判定する日付
 * @returns 祝日または日曜日の場合true
 */
export function isHolidayOrSunday(date: Date): boolean {
  return isHoliday(date) || isSunday(date)
}

